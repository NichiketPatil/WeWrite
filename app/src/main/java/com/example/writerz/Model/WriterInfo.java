package com.example.writerz.Model;

public class WriterInfo {

    private String b;
    private String c;
    private String e;
    private String f;
    private String n;
    private String o;
    private String t;


    public WriterInfo(String b, String c, String e, String f, String n, String o, String t) {
        this.b = b;
        this.c = c;
        this.e = e;
        this.f = f;
        this.n = n;
        this.o = o;
        this.t = t;
    }

    public WriterInfo() {


    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public String getE() {
        return e;
    }

    public void setE(String e) {
        this.e = e;
    }

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getO() {
        return o;
    }

    public void setO(String o) {
        this.o = o;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }
}
